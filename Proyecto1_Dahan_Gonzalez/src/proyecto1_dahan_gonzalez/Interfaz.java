    private void contratarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contratarRActionPerformed
        // TODO add your handling code here:
        
       boolean aux = false;
        if(this.logica.getProvInicialR()< this.logica.getMaxProvR()){
            this.logica.setProvInicialR(this.logica.getProvInicialR()+1);
            this.logica.getInterfaz().getProdRuedas().setText(Integer.toString(this.logica.getProvInicialR()));
        }
        
        for (int i = 0; i < this.logica.getProvInicialR(); i++) {
            if (this.logica.getpRuedas()[i]== null && !aux) {
                System.out.println("SE CONTRATO A UN PROVEEDOR DE RUEDAS");
               this.logica.getpRuedas()[i] = new Proveedores (this.logica.getSEMR(), this.logica.getSPR(), this.logica.getSCR(), this.logica.getAlamacenRuedas(), 1, this.logica);
               this.logica.getpRuedas()[i].start();
               aux = true;
               break;
            }
        }
    }//GEN-LAST:event_contratarRActionPerformed

