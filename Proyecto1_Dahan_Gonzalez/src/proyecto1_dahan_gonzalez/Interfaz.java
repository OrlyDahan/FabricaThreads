    private void contratarEnsambladoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contratarEnsambladoresActionPerformed
        // TODO add your handling code here:
        boolean aux = false;
        if(this.logica.getEnsInicial()< this.logica.getMaxEns()){
            this.logica.setEnsInicial(this.logica.getEnsInicial()+1);
            this.logica.getInterfaz().getCantEnsam().setText(Integer.toString(this.logica.getEnsInicial()));
        }
        
        for (int i = 0; i < this.logica.getEnsInicial(); i++) {
            if (this.logica.getEnsamblador()[i]== null && !aux) {
                System.out.println("SE CONTRATO A UN ENSAMBLADOR");
               this.logica.getEnsamblador()[i] =new Ensamblador (this.logica.getSEMR(), this.logica.getSPR(), this.logica.getSCR(), this.logica.getSEMP(), this.logica.getSPP(), this.logica.getSCP(),this.logica.getSEMM(), this.logica.getSPM(), this.logica.getSCM(), this.logica, this.logica.getSEMD());
               this.logica.getEnsamblador()[i].start();
               aux = true;
               break;
            }
        }
    }//GEN-LAST:event_contratarEnsambladoresActionPerformed

